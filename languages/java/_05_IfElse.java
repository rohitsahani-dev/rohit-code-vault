public class _05_IfElse {
// If-else condition is used to execute a block of code if a specified condition is true, and another block of code if the condition is false.
    public static void main(String[] args) {
        int age = 18;
// The if statement checks if the condition (age >= 18) is true. If it is true, it executes the block of code inside
        if (age >= 18) {
            System.out.println("Adult");
        } else {
            System.out.println("Minor");
        }
    }

}

