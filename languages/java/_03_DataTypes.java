// Basic data types

public class _03_DataTypes {
    public static void main(String[] args) {
        int a = 10;
        double b = 3.14;
        char c = 'A';
        boolean isTrue = true;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(isTrue);
    }
}