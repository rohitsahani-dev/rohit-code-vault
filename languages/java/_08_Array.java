 // Arrays in Java
public class _08_Array {
    public static void main(String[] args) {
        // An array is a data structure that can hold a fixed number of values of the same type.
        String[] fruits = {"Apple", "Banana", "Mango"};
        // The for-each loop is used to iterate through each element in the array. 
        // It simplifies the code and makes it easier to read.
        for (String fruit : fruits) {
            System.out.println(fruit);
        }
    }
} 