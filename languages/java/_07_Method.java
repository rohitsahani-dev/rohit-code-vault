public class _07_Method {
    // Methods (functions)
    // Methods in Java are blocks of code that perform a specific task. 
    // They are defined within a class and can be called to execute the code they contain.
    static void greet(String name) {
        System.out.println("Hello " + name);
    }

    public static void main(String[] args) {
        greet("Rohit");   // Calling the greet method with the argument "Rohit"
    }
}

